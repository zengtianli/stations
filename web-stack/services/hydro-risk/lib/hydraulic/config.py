def get_river_name(river_code):
    """根据河流编码获取名称"""
    if not river_code:
        return None
    code_upper = str(river_code).strip().upper()
    return RIVER_CODE_MAPPING.get(code_upper)


def get_city_from_county(county_str, separator=',', unique=True):
    """
    根据县区名称获取地级市名称

    支持单个县区或分隔符分隔的多个县区。

    Examples:
        >>> get_city_from_county("德清县")
        '湖州市'
        >>> get_city_from_county("德清县,临安区,南浔区")
        '湖州市,杭州市'
    """
    if not county_str:
        return None

    counties = [c.strip() for c in str(county_str).split(separator) if c.strip()]
    cities = []
    for county in counties:
        city = COUNTY_TO_CITY.get(county)
        if city:
            cities.append(city)

    if not cities:
        return None

    if unique:
        seen = set()
        cities = [c for c in cities if not (c in seen or seen.add(c))]

    return separator.join(cities)
